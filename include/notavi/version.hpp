#ifndef NOTAVI_VERSION_HPP_
#define NOTAVI_VERSION_HPP_

#define NOTAVI_VERSION_MAJOR @PROJECT_VERSION_MAJOR @
#define NOTAVI_VERSION_MINOR @PROJECT_VERSION_MINOR @
#define NOTAVI_VERSION_PATCH @PROJECT_VERSION_PATCH @

namespace notavi {
inline void get_version(unsigned& major, unsigned& minor, unsigned& patch)
{
  major = NOTAVI_VERSION_MAJOR;
  minor = NOTAVI_VERSION_MINOR;
  patch = NOTAVI_VERSION_PATCH;
}
} // namespace notavi

#endif // NOTAVI_VERSION_HPP_
